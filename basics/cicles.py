# Цикл while
i = 0
while i < 3:
    i += 1
    print(i)
print("Вышли из цикла")

# Цикл for
list_ = [1, 2, 3, 66, 13]
for el in list_:
    print(el)

# Операторы break и continue внутри цикла for, а также слово else после него, работают точно так же, как и с циклом
# while.
# in в цикле — это не тот оператор in, про который ищет подстроку в строке. for вместе с
# in — конструкция для создания цикла, то есть воспринимать ее надо как единое целое.
