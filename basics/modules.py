# Чтобы использовать библиотеку в своей программе, ее нужно импортировать с помощью оператора import. Например,
# эта программа выведет на экран текущую дату и время:
import time
print(time.asctime())

# А эта — значение обратного гиперболического тангенса от числа 0.5:
import math
print(math.atanh(0.5))
