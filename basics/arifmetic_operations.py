# Сложение
a = 5 + 1
# Вычитание
b = 6 - 5
# Умножение
c = 8 * 3
# Деление
d = 8 / 2
# Деление на цело
e = 8 // 3
# Остаток от деления
f = 9 % 2
# Возведение в степень
g = 2 ** 3

print(a, b, c, d, e, f, g)

# Самая приоритетная из арифметических операций — возведение в степень. Потом идут умножение, деление, целочисленное
# деление и остаток от деления. И в самом конце — сложение и вычитание.

# Для изменения порядка вычисления можно использовать скобки.
